'''
718. Maximum Length of Repeated Subarray

Given two integer arrays A and B, return the maximum length of an subarray that appears in both arrays.

Example 1:
Input:
A: [1,2,3,2,1]
B: [3,2,1,4,7]
Output: 3
Explanation: 
The repeated subarray with maximum length is [3, 2, 1].
Note:
1 <= len(A), len(B) <= 1000
0 <= A[i], B[i] < 100
'''


class Solution(object):
    def findLength(self, A, B):
        memo = [[0] * (len(B) + 1) for _ in range(len(A) + 1)]
        for i in range(len(A) - 1, -1, -1):
            for j in range(len(B) - 1, -1, -1):
                if A[i] == B[j]:
                    memo[i][j] = memo[i+1][j+1]+1
        return max(max(row) for row in memo)


class Solution:
    def findLength(self, A, B):
        """
        :type A: List[int]
        :type B: List[int]
        :rtype: int
        """

        a_str = ''.join(list(map(chr, A)))
        b_str = ''.join(list(map(chr, B)))
        a_size = len(A)
        b_size = len(B)

        def check(length):
            a_set = set([a_str[i:i + length] for i in range(a_size - length + 1)])
            return any(b_str[i:i + length] in a_set for i in range(b_size - length + 1))

        start, end = 0, min(a_size, b_size)
        while start + 1 < end:
            mid = start + (end - start) // 2
            if check(mid):
                start = mid
            else:
                end = mid - 1

        # now start+1==end or start == end
        # max length -> check end first
        if check(end):
            return end
        else:
            return start


